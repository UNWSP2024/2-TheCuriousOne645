# Input ages of five friends
age1 = float(input("Enter the age of friend 1: 55"))
age2 = float(input("Enter the age of friend 2: 20"))
age3 = float(input("Enter the age of friend 3: 18"))
age4 = float(input("Enter the age of friend 4: 19"))
age5 = float(input("Enter the age of friend 5: 13"))

# Calculate average age
average_age = (age1 + age2 + age3 + age4 + age5) / 5

